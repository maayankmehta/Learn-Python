#comparators

#assignment operator =
x = 15
print(x)
#output : 15

#equal to ==
print(10 == 10)
#output: True
print(10 == 9)
#output:False

#not equal !=
print(10 != 10)
#output:False

#less than <
print(10 < 9)
#output:False

#greater than >
print(10 > 9)
#output: True

#less than equal to <=
print(10 <= 9)
#output: False

#greater than equal to >=
print(10 >= 0)
#output: True
